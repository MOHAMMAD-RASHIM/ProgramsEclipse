package com.tap.ifelse;
public class ThreePairSum {

	public static void main(String[] args) {
		// Input 
		int a = 10;
		int b = 20;
		int c = 30;
		
		// Logic
		int sum1 = a+b;
		int sum2 = a+c;
		int sum3 = b+c;
		
		// Output
		System.out.println(sum1);
		System.out.println(sum2);
		System.out.printf("%.4f",sum3);

	}

}
